export const getImageForQuery = (query) => {
  if (!query) return FALLBACK_IMAGES[0];
  const q = query.toLowerCase();
  for (const [key, url] of Object.entries(PLACE_IMAGES)) {
    if (q.includes(key)) return url;
  }
  let hash = 0;
  for (let c of q) hash = (hash * 31 + c.charCodeAt(0)) % FALLBACK_IMAGES.length;
  return FALLBACK_IMAGES[Math.abs(hash)];
};

