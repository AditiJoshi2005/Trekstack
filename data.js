export function generateTransports(mode, cities, date) {
  const companies = {
    flight: ['IndiGo', 'Air India', 'SpiceJet', 'Vistara', 'GoFirst', 'Akasa Air', 'Air India Express', 'Blue Dart Aviation'],
    train:  ['Rajdhani Express', 'Shatabdi Express', 'Vande Bharat Express', 'Duronto Express', 'Garib Rath Express', 'Jan Shatabdi Express', 'Superfast Express', 'Humsafar Express'],
    bus:    ['KSRTC Airavat Volvo', 'VRL Travels', 'Orange Travels', 'SRS Travels', 'Parveen Travels', 'IntrCity SmartBus', 'RedBus Partner', 'Neeta Tours'],
  };
  const basePrice = { flight: 4200, train: 850, bus: 550 };
  const list = [];
  const cos = companies[mode];
  const seatTypes = {
    flight: ['Economy', 'Economy Flexi', 'Business', 'First Class'],
    train:  ['Sleeper (SL)', '3 AC (3A)', '2 AC (2A)', '1 AC (1A)', 'Chair Car (CC)'],
    bus:    ['Seater', 'Semi-Sleeper', 'Sleeper', 'AC Sleeper', 'Volvo Multi-Axle'],
  };
  const amenitiesPool = {
    flight: [['Meal', 'USB Charging', 'WiFi'], ['Meal'], ['Meal', 'WiFi', 'USB', 'Extra Legroom'], ['Priority Boarding', 'Lounge Access', 'Meal', 'WiFi']],
    train:  [['Pantry Car', 'Charging Point'], ['Bedding', 'Pantry', 'Charging'], ['AC', 'Bedding', 'Charging'], ['Meals Included', 'Premium Bedding']],
    bus:    [['WiFi', 'Charging', 'Water Bottle'], ['AC', 'Blanket', 'Charging'], ['WiFi', 'Blanket', 'Snacks', 'Charging'], ['Luxury Recline', 'Meal', 'WiFi', 'Charging']],
  };
  for (let i = 0; i < 8; i++) {
    const depH = 5 + i * 2;
    const durH = mode === 'flight' ? (2 + (i % 3)) : mode === 'train' ? (5 + (i % 5)) : (8 + (i % 4));
    const depM = i % 2 === 0 ? '00' : '30';
    const arrH = (depH + durH) % 24;
    const price = Math.round((basePrice[mode] + (Math.random() * 2500)) / 50) * 50;
    const sType = seatTypes[mode][i % seatTypes[mode].length];
    const avail = Math.floor(Math.random() * 60) + 5;
    // Generate seat map
    const seats = [];
    const rows = mode === 'flight' ? 12 : mode === 'train' ? 18 : 10;
    const cols = mode === 'flight' ? ['A','B','C','D','E','F'] : mode === 'train' ? ['L1','M1','U1','L2','M2','U2'] : ['W1','M1','W2'];
    for (let r = 1; r <= rows; r++) {
      cols.forEach(col => {
        const taken = Math.random() > 0.45;
        const seatPrice = price + (col.includes('W') || col === 'A' || col === 'F' ? 200 : 0);
        seats.push({
          id: `${r}${col}`,
          row: r,
          col,
          type: col === 'A' || col === 'F' || col.includes('W') ? 'Window' : col === 'C' || col === 'D' || col.includes('M') ? 'Middle' : 'Aisle',
          price: seatPrice,
          available: !taken,
        });
      });
    }
    list.push({
      id: `${mode}-${i}`,
      company: cos[i % cos.length],
      departure: `${String(depH).padStart(2, '0')}:${depM}`,
      arrival: `${String(arrH).padStart(2, '0')}:${depM}`,
      duration: `${durH}h ${depM === '30' ? '30' : '00'}m`,
      price,
      class: sType,
      availableSeats: avail,
      rating: (3.5 + Math.random() * 1.4).toFixed(1),
      amenities: amenitiesPool[mode][i % amenitiesPool[mode].length],
      seats,
    });
  }
  return list.sort((a, b) => a.price - b.price);
}

export function generateHotels(pkg, budget, stars = 2) {
  const hotelPrefixes = ['The Grand', 'Royal', 'Heritage', 'Comfort', 'Luxury', 'Boutique', 'Eco', 'Palace'];
  const hotelSuffixes = ['Resort', 'Inn', 'Suites', 'Residency', 'Haveli', 'Lodge', 'Hotel', 'Retreat'];
  const hotels = [];
  const cities = pkg?.cities || ['Destination'];
  for (let i = 0; i < 8; i++) {
    const city = cities[i % cities.length];
    const starRating = Math.min(5, Math.max(1, stars) + (i % 3));
    const baseNightPrice = Math.round((budget * 0.35 / 3) * (0.6 + i * 0.12) / 50) * 50;
    const roomTypes = [
      { type: 'Standard Room',   desc: 'Cozy room with all basic amenities',      price: baseNightPrice },
      { type: 'Deluxe Room',     desc: 'Spacious room with city/garden view',      price: Math.round(baseNightPrice * 1.35 / 50) * 50 },
      { type: 'Suite',           desc: 'Premium suite with lounge & premium view', price: Math.round(baseNightPrice * 1.9 / 50) * 50 },
    ];
    hotels.push({
      id: `hotel-${i}`,
      name: `${hotelPrefixes[i % hotelPrefixes.length]} ${city} ${hotelSuffixes[i % hotelSuffixes.length]}`,
      city,
      stars: starRating,
      price: baseNightPrice,
      rating: (3.4 + Math.random() * 1.5).toFixed(1),
      imgKey: city.toLowerCase(),
      amenities: ['Free WiFi', 'AC Room', 'Hot Water', 'Room Service', 'Parking', 'Restaurant', 'Swimming Pool', 'Gym'].slice(0, 4 + (i % 4)),
      address: `Near ${['Main Market', 'City Centre', 'Railway Station', 'Bus Stand', 'Airport Road'][i % 5]}, ${city}`,
      cancellation: i % 2 === 0 ? 'Free cancellation till 24h before check-in' : 'Non-refundable booking',
      checkIn: '12:00 PM',
      checkOut: '11:00 AM',
      roomTypes,
    });
  }
  return hotels.filter(h => h.stars >= Math.max(1, stars)).sort((a, b) => a.price - b.price);
}
